# 基本语法
# 1，在Python中严格区分大小写
# 2，Python中的每一行就是一条语句，每条语句以换行结束，（没有分号）
# 3，Python中每一行语句不要过长（规范中建议txtf不要超过80个字符）
# 4，一条语句可以分多行编写，语句后边以反斜杠结尾
# 5，Python是缩进严格的语言，所以在Python中不要随便写缩进
# 6，在Python中使用#来表示注释，#后的内容都属于注释，注释的内容将会被解释器所忽略，
#   我们可以通过注释来对程序进行解释说明，一定要养成良好的编写注释的习惯，注释
#   要求简单明了，一般习惯上#后边会跟着一个空格

