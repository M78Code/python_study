# 练习1:
#   求100以内所有的奇数之和
i = 0   # 索引下标
sum = 0     # 和
while i <= 100:
    if i % 2 != 0:
        print(f"i : {i}")
        sum += i
    i += 1
else:
    print(f"100以内所有奇数的和 sum = {sum}")

# 练习2:
#   求100以内所有7的倍数之和，以及个数
i = 1       # index
sum = 0     # sum
count = 0   # count
while i<=100:
    if(i % 7 == 0):
        print(f"7的倍数: {i}")
        count += 1  # 记录个数 
        sum += i    # 累加和
    i += 1      # update index
else:
    print(f"100以内所有7的倍数之和 sum = {sum}, 这种数有 count = {count}")
    
